#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'z')
		{
			if (str[i] <= 'Z' || str[i] >= 'a')
			{
				i++;
			}
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
